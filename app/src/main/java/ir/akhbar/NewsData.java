package ir.akhbar;

public class NewsData {

    private String title;
    private String description;
    private String urlToImage;
    private String url;

    public String getNewsTitle() {
        return title;
    }

    public String getNewsDescription() {
        return description;
    }

    public String getNewsImage() {
        return urlToImage;
    }

    public String getUrl() {
        return url;
    }
}
